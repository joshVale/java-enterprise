public class Employee {
    int age;
    String name;
    String surname;
    String position;
    String phoneNumber;
    String email;
    public Employee(int age,String name,String surname,String phoneNumber,String position,String email){
        this.age = age;
        this.name = name;
        this.surname = surname;
        this.position = position;
        this.phoneNumber = phoneNumber;
        this.email = email;
    }
}
